-- Procedure
-- Departmana zam yapan prosedür
CREATE OR REPLACE PROCEDURE PR_GIVE_RAISE(
    P_DEPTNO     IN NUMBER,
    P_PERCENTAGE IN NUMBER
) AS
BEGIN
    UPDATE EMPLOYEE
    SET SALARY = SALARY + (SALARY * P_PERCENTAGE / 100)
    WHERE DEPTNO = P_DEPTNO;
    COMMIT;
END;
